package com.nlu.dao;

public class MonHoc {
	private  int maMon;
	private 	String tenMon;
	public MonHoc(int maMon, String tenMon) {
		super();
		this.maMon = maMon;
		this.tenMon = tenMon;
	}
	public int getMaMon() {
		return maMon;
	}
	public void setMaMon(int maMon) {
		this.maMon = maMon;
	}
	public String getTenMon() {
		return tenMon;
	}
	public void setTenMon(String tenMon) {
		this.tenMon = tenMon;
	}
	
	
	

}
